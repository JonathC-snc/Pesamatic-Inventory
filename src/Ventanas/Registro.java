    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int validacion = 0;
        String user, nombre, correo, password;
        boolean cargo;

        user = txt_usuario.getText().trim();
        nombre = txt_nombre.getText().trim();
        correo = txt_correo.getText().trim();
        password = txt_password.getText().trim();
        cargo = chkadmin.getVerifyInputWhenFocusTarget();

        if (user.equals("")) {
            txt_usuario.setBackground(Color.red);
            validacion++;
        }
        if (nombre.equals("")) {
            txt_nombre.setBackground(Color.red);
            validacion++;
        }
        if (correo.equals("")) {
            txt_correo.setBackground(Color.red);
            validacion++;
        }
        if (password.equals("")) {
            txt_password.setBackground(Color.red);
            validacion++;
        }
        if (chkadmin.equals(true)){
            chkadmin.setBackground(Color.red);
            validacion++;
        }
        if (chkempleado.equals(true)){
            chkempleado.setBackground(Color.red);
            validacion++;
        }
        

        if (validacion == 0) {

            try {
                Connection conectar = Conexion.conectar();
                PreparedStatement pst = conectar.prepareStatement(
                    "select user from usuarios where user = '" + user + "'");

                ResultSet rst = pst.executeQuery();

                if (rst.next()) {

                    txt_usuario.setBackground(Color.red);
                    JOptionPane.showMessageDialog(null, "Usuario ya registrado");
                    txt_usuario.setText("");
                    txt_nombre.setText("");
                    txt_correo.setText("");
                    txt_password.setText("");
                    chkadmin.setSelected(false);
                    chkempleado.setSelected(false);

                } else {

                    Connection conectar2 = Conexion.conectar();
                    PreparedStatement pst2 = conectar2.prepareStatement(
                        "insert into usuarios values(?,?,?,?,?,?)");

                    pst2.setString(1, "0");
                    pst2.setString(2, user);
                    pst2.setString(3, password);
                    pst2.setString(4, nombre);
                    pst2.setString(5, correo);
                    pst2.setBoolean(6, cargo);
                    pst2.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Resgistro Completo");
                    dispose();
                    new Login_Pesamatic().setVisible(true);
                }

            } catch (SQLException e) {
                System.err.println("Error al agregar usuario" + e);
                JOptionPane.showMessageDialog(null, "No se logro agregar el usuario");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe Rellenar todos los campos");
            txt_usuario.setBackground(Color.white);
            txt_nombre.setBackground(Color.white);
            txt_correo.setBackground(Color.white);
            txt_password.setBackground(Color.white);
            chkadmin.setSelected(false);
            chkempleado.setEnabled(false);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

